package org.stock.controllers;

import org.stock.models.Sector;

public class SectorRound {
	private int round;
	private Sector sector;
	
	public int getRound() {
		return round;
	}
	public void setRound(int round) {
		this.round = round;
	}
	public Sector getSector() {
		return sector;
	}
	public void setSector(Sector sector) {
		this.sector = sector;
	}
	

}
